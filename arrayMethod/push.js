 //push - it will add elements in last of array
 
 let arr = ['hello','iam']

 arr.push('here')
 console.log(arr);

 //unshift - it will add elements in first of an array

 let arr1   = ['iam ','rizwan']

 arr1.unshift('hey')
 console.log(arr1)

 // pop - it will remove element from last of an array

 let array = ['iam ','note','there','boo']

array.pop()
console.log(array);

//shift - it will remove first element from array

let arr2 = ['bujjaa','who','are','you']

arr2.shift()

console.log(arr2);

